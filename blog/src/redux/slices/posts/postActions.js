import { createAsyncThunk, createAction } from "@reduxjs/toolkit";
import axios from "axios";
import baseUrl from "../../../utils/baseURL";

//action to redirect
export const resetPost = createAction("category/reset");
export const resetPostEdit = createAction("post/reset");
export const resetPostDelete = createAction("post/delete");

//Create
export const createpostAction = createAsyncThunk(
  "post/created",
  async (post, { rejectWithValue, getState, dispatch }) => {
    //get user token
    const user = getState()?.users;
    const { userAuth } = user;
    const config = {
      headers: {
        Authorization: `Bearer ${userAuth?.token}`,
      },
    };

    console.log('post', post);
    try {
      const postDetails = {
        tieu_de: post?.title,
        noi_dung: post?.description,
        nhom_tin_tuc_id: post?.category,
      }
      
      //http call
      const { data } = await axios.post(
        `${baseUrl}/api/post`,
        postDetails,
        config
      );
      console.log('data', data);

      //dispatch action
      dispatch(resetPost());
      return data;
    } catch (error) {
      if (!error?.response) throw error;
      return rejectWithValue(error?.response?.data);
    }
  }
);

